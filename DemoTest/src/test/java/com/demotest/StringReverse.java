package com.demotest;

public class StringReverse {

	public static void main(String[] args) {

		
		//Revers String 
		String aka = "Akash";
		int len = aka.length();

		String rev = "";

		for (int i = len - 1; i >= 0; i--) {

			rev = rev + aka.charAt(i);
		}
		System.out.println(rev);
	}
	
	
	//Reverse int:
	

	
	
	
}
	

